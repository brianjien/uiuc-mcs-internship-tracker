import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const distDir = path.join(projectRoot, "dist");
const outputFile = path.join(projectRoot, "edge", "static-assets.generated.mjs");

const mimeTypes = new Map([
  [".html", "text/html; charset=utf-8"],
  [".css", "text/css; charset=utf-8"],
  [".js", "text/javascript; charset=utf-8"],
  [".mjs", "text/javascript; charset=utf-8"],
  [".json", "application/json; charset=utf-8"],
  [".svg", "image/svg+xml"],
  [".png", "image/png"],
  [".jpg", "image/jpeg"],
  [".jpeg", "image/jpeg"],
  [".webp", "image/webp"],
  [".ico", "image/x-icon"],
  [".woff", "font/woff"],
  [".woff2", "font/woff2"],
]);

async function walk(directory) {
  const entries = await fs.readdir(directory, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(directory, entry.name);
      return entry.isDirectory() ? walk(fullPath) : fullPath;
    }),
  );
  return files.flat();
}

async function buildAssetModule() {
  const files = (await walk(distDir)).sort();
  if (!files.length) {
    throw new Error("No files found in dist. Run vite build before generating edge assets.");
  }

  const assets = {};
  let totalBytes = 0;

  for (const filePath of files) {
    const body = await fs.readFile(filePath);
    const routePath = `/${path.relative(distDir, filePath).split(path.sep).join("/")}`;
    assets[routePath] = {
      type: mimeTypes.get(path.extname(filePath).toLowerCase()) || "application/octet-stream",
      body: body.toString("base64"),
      bytes: body.byteLength,
    };
    totalBytes += body.byteLength;
  }

  await fs.mkdir(path.dirname(outputFile), { recursive: true });
  await fs.writeFile(
    outputFile,
    `// Generated by scripts/build-edge-assets.mjs. Do not edit by hand.\nexport const assets = ${JSON.stringify(
      assets,
      null,
      2,
    )};\n`,
    "utf8",
  );

  console.log(`Generated ${files.length} Wasmer edge assets (${(totalBytes / 1024 / 1024).toFixed(2)} MB).`);
}

await buildAssetModule();
